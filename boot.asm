ORG 0
BITS 16

JMP SHORT START
NOP
TIMES 33 DB 0

START:
    JMP 0X7C0:START_2
START_2:
    CLI
    MOV AX,0X7C0
    MOV DS,AX
    MOV ES,AX
    MOV AX,0
    MOV SS,AX
    MOV SP,0X7C00
    STI

    MOV AH,02H
    MOV AL,1
    MOV CH,0
    MOV CL,2
    MOV DH,0
    MOV BX,BUFFER
    INT 13H

    JC ERROR
    
    MOV SI,BUFFER
    CALL PRINT
    JMP LAST

ERROR:
    MOV SI,ERROR_MESSAGE
    CALL PRINT

LAST:   JMP $


PRINT: 
    MOV BX,0
.LOOP:
    LODSB
    CMP AL,0
    JZ .BREAK
    CALL PRINT_CHAR
    JMP .LOOP
.BREAK:
    RET

PRINT_CHAR:
    MOV AH,0EH
    INT 10H
    RET

ERROR_MESSAGE:  DB  "FAILED TO LOAD SECTOR",0

TIMES 510-($-$$) DB 0
DW  0XAA55

BUFFER:

